abstract class MovieItemModel {
  String date;
  String genre;
  String link;
  String poster;
  String synopsis;
  String synopsisFull;
  String title;

  MovieItemModel({
    this.date,
    this.genre,
    this.link,
    this.poster,
    this.synopsis,
    this.synopsisFull,
    this.title,
  });
}
